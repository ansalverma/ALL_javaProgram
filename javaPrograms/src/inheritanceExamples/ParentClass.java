package inheritanceExamples;

public class ParentClass {

	public ParentClass()
	{
		System.out.println("Default parent constructor");
	}
	public ParentClass(int a)
	{
		System.out.println("One param parent constructor");
	}
	public ParentClass(int a,int b)
	{
		System.out.println("Two param parent constructor");
	}
	public ParentClass(int a,int b,int c)
	{
		System.out.println("Three param parent constructor");
	}
	public ParentClass(int a,int b,int c,int d)
	{
		System.out.println("Four param parent constructor");
	}
}
